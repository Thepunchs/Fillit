#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*str;
	int		len;

	len = ft_strlen(src);
	if (!(str = (char*)malloc(sizeof(*str) * len + 1)))
		return (NULL);
	i = 0;
	while (i < len)
	{
		((unsigned char *)str)[i] = ((unsigned char *)src)[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
